# integer variables
a  = 10
b = 3

print ("INTEGER OPERATIONS")
print ("addition:", a + b)
print ("subtraction:", a - b)
print ("multiplication:", a * b)
print ("division:", a / b)
print ("modulus:", a % b)
print ("exponent:", a ** b)
print()

# float variables
x = 10.5
y = 2.5

print ("FLOAT OPERATIONS")
print ("addition:", x + y)
print ("subtraction:", x - y)
print ("multiplication:", x * y)
print ("division:", x / y)
print ("modulus:", x % y)
print ("exponent:", x ** y)
print()

# complex variables
c1 = 2 + 3j
c2 = 1 + 4j

print ("COMPLEX OPERATIONS")
print ("addition:", c1 + c2)
print ("subtraction:", c1 - c2)
print ("multiplication:", c1 * c2)
print ("division:", c1 / c2)
# modulus and exponent operations are not defined for complex numbers


