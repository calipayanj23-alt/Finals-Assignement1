#PART 2: CONDITIONS WITH STRINGS, INT, AND BOOLEAN DATA TYPE
#LIST: ORDERDED, MUTABLE COLLECTION

my_list = ["apple:", 42, True, "banana", 99, False]
print ("List:", my_list)
print ("list length:", len(my_list))

#TUPLE: ORDERED, IMMUTABLE COLLECTION
my_tuple = ("carrot:", 7, True, "date", 13, False)
print ("Tuple:", my_tuple)
print ("tuple length:", len(my_tuple))

#SET UNORDERED, MUTABLE COLLECTION OF UNIQUE ELEMENTS
my_set = {"elephant", 22, True, "cat", 13, False}
print ("Set:", my_set)
print ("set length:", len(my_set))